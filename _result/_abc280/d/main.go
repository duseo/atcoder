func fig(pf, k int) bool {
	fac := 1
	for i := 2; i <= pf; i++ {
		fac *= i
		if fac >= k {
			return true
		}
	}
	return false
}

func rec(k int) int {
	if k == 0 {
		return 1
	}

	return k * rec(k-1)
}

